// A simple Rust function that takes two numbers as arguments and returns their sum
fn add(a: i32, b: i32) -> i32 {
    a + b
}

// A function to print a message on the console
fn print_message() {
    println!("This is a Rust program!");
}

// A function that takes no arguments and returns nothing
fn main() {
    add(1, 2);
    print_message();
}
